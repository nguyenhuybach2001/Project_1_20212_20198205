    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(ftxSbd.getText().equals("")){
            sb.append("Sbd khong duoc de trong!!!\n");
            ftxSbd.setBackground(Color.red);
        } else{
            ftxSbd.setBackground(Color.WHITE);
        }
        if(txtHovaTen.getText().equals("")){
            sb.append("Ho va ten khong duoc de trong!!!\n");
            txtHovaTen.setBackground(Color.red);
        }else{
            txtHovaTen.setBackground(Color.WHITE);
        }
        if (sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(), "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int choice = JOptionPane.showConfirmDialog(this, "Ban co muon sua khong?","Hoi",JOptionPane.YES_NO_OPTION);
        if ( choice == JOptionPane.NO_OPTION){
            return;
        }
        
        for (ThiSinh thisinh : list){
            if (thisinh.getSbd().equals(ftxSbd.getText())){
                
                thisinh.setName(txtHovaTen.getText());
                thisinh.setDiachi(txtDiachi.getText());
                thisinh.setKhoi(cbxKhoi.getSelectedItem().toString());
                thisinh.setUutien(Integer.parseInt(txtUutien.getText()));
            break;
            }
            
        }
        fillTable();
        
        txtHovaTen.setText("");
        txtDiachi.setText("");
        ftxSbd.setText("");
        txtUutien.setText("");
        cbxKhoi.setSelectedIndex(0);
    }//GEN-LAST:event_btnSuaActionPerformed


    private void fillTable(){  
        tblModel.setRowCount(0);
        
        for(ThiSinh thisinh : list){
            tblModel.addRow(new Object[]{thisinh.getName(), thisinh.getSbd(), thisinh.getDiachi(),thisinh.getKhoi(), thisinh.getUutien()});
        }
        tblModel.fireTableDataChanged();
    }
    

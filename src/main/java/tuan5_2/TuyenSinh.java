    private void tblThiSinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblThiSinhMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblThiSinh.getSelectedRow();
        
        if (selectedRow>=0){
            String Sbd= (String) tblThiSinh.getValueAt(selectedRow, 1);
            for (ThiSinh thisinh : list){
                if(Sbd.equals(thisinh.getSbd())){
                   txtHovaTen.setText(thisinh.getName());
                   ftxSbd.setText(Sbd);
                   txtDiachi.setText(thisinh.getDiachi());
                   cbxKhoi.setSelectedItem(thisinh.getKhoi());
                   txtUutien.setText(""+thisinh.getUutien());
                   break;
                }
            }
        }
    }//GEN-LAST:event_tblThiSinhMouseClicked

